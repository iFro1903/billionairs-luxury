/**
 * Translate login page elements
 */
function translateLoginPage() {
    const currentLang = window.i18n?.currentLang || 'en';
    const translations = loginTranslations[currentLang] || loginTranslations['en'];
    
    console.log('ðŸ”„ Translating login page to:', currentLang);
    
    // Page title
    document.title = translations.page_title;
    
    // Main title
    const loginTitle = document.querySelector('.login-title');
    if (loginTitle) {
        loginTitle.textContent = translations.access_portal;
        console.log('âœ“ Title translated to:', translations.access_portal);
    }
    
    // Subtitle
    const loginSubtitle = document.querySelector('.login-subtitle');
    if (loginSubtitle) {
        loginSubtitle.textContent = translations.enter_exclusive;
        console.log('âœ“ Subtitle translated to:', translations.enter_exclusive);
    }
    
    // Email label
    const emailLabel = document.querySelector('label[for="loginEmail"]');
    if (emailLabel) {
        emailLabel.textContent = translations.email_label;
        console.log('âœ“ Email label translated');
    }
    
    // Password label
    const passwordLabel = document.querySelector('label[for="loginPassword"]');
    if (passwordLabel) {
        passwordLabel.textContent = translations.password_label;
        console.log('âœ“ Password label translated');
    }
    
    // Forgot password link
    const forgotLink = document.getElementById('forgotPasswordLink');
    if (forgotLink) {
        forgotLink.textContent = translations.forgot_password;
        console.log('âœ“ Forgot password link translated');
    }
    
    // Enter button
    const enterBtn = document.querySelector('.submit-button');
    if (enterBtn && !enterBtn.id) {
        enterBtn.textContent = translations.enter_button;
        console.log('âœ“ Enter button translated');
    }
    
    // Reset password modal
    const resetTitle = document.querySelector('#forgotPasswordModal h2');
    if (resetTitle) resetTitle.textContent = translations.reset_password_title;
    
    const resetText = document.querySelector('#forgotPasswordModal p');
    if (resetText) resetText.textContent = translations.reset_password_text;
    
    const forgotEmailLabel = document.querySelector('label[for="forgotEmail"]');
    if (forgotEmailLabel) forgotEmailLabel.textContent = translations.email_label;
    
    const sendResetBtn = document.getElementById('sendResetBtn');
    if (sendResetBtn) sendResetBtn.textContent = translations.send_reset_link;
    
    const backToLoginLink = document.getElementById('backToLoginLink');
    if (backToLoginLink) backToLoginLink.textContent = translations.back_to_login;
    
    // Back to home link
    const backLink = document.querySelector('.back-link a');
    if (backLink) {
        backLink.textContent = translations.back_to_home;
        console.log('âœ“ Back link translated');
    }
    
    console.log('âœ… Login page fully translated to', currentLang);
}

// Make function globally accessible
window.translateLoginPage = translateLoginPage;

// Listen for language changes
window.addEventListener('languageChanged', (event) => {
    console.log('ðŸŒ Language changed event received on login page:', event.detail.language);
    setTimeout(translateLoginPage, 50);
});

window.addEventListener('i18nReady', () => {
    console.log('ðŸŒ i18n ready event received on login page');
    setTimeout(translateLoginPage, 100);
});

// Initial translation when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        setTimeout(translateLoginPage, 150);
    });
} else {
    setTimeout(translateLoginPage, 150);
}
